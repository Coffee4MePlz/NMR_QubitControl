function U = qftgate(nq);
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%U = qftgate(nq);
% QFT of nq qubits;
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%% 


N = 2^nq;

for m = 1:1:N;
   for n = 1:1:N;
      
	 U(n,m) = exp(2 * pi * i * (n-1) * (m-1) / N);
   
	end;

end;

U = U / sqrt(N);







